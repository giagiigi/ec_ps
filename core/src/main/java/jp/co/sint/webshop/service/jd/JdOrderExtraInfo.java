public class JdOrderExtraInfo {

  private boolean bjFlag = false;

  private boolean shFlag = false;

  private boolean gzFlag = false;

  /**
   * @return the bjFlag
   */
  public boolean isBjFlag() {
    return bjFlag;
  }

  /**
   * @param bjFlag
   *          the bjFlag to set
   */
  public void setBjFlag(boolean bjFlag) {
    this.bjFlag = bjFlag;
  }

  /**
   * @return the shFlag
   */
  public boolean isShFlag() {
    return shFlag;
  }

  /**
   * @param shFlag
   *          the shFlag to set
   */
  public void setShFlag(boolean shFlag) {
    this.shFlag = shFlag;
  }

  /**
   * @return the gzFlag
   */
  public boolean isGzFlag() {
    return gzFlag;
  }

  /**
   * @param gzFlag
   *          the gzFlag to set
   */
  public void setGzFlag(boolean gzFlag) {
    this.gzFlag = gzFlag;
  }

}
